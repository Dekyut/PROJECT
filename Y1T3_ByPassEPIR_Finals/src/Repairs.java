
public class Repairs {
	
	public String customerName, customerEmail, productIssue, dateReceived, dateCompletion, productStatus;
	public int customerNumber;
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerEmail() {
		return customerEmail;
	}
	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	public String getProductIssue() {
		return productIssue;
	}
	public void setProductIssue(String productIssue) {
		this.productIssue = productIssue;
	}
	public String getDateReceived() {
		return dateReceived;
	}
	public void setDateReceived(String dateReceived) {
		this.dateReceived = dateReceived;
	}
	public String getDateCompletion() {
		return dateCompletion;
	}
	public void setDateCompletion(String dateCompletion) {
		this.dateCompletion = dateCompletion;
	}
	public String getProductStatus() {
		return productStatus;
	}
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	public int getCustomerNumber() {
		return customerNumber;
	}
	public void setCustomerNumber(int customerNumber) {
		this.customerNumber = customerNumber;
	}

	
}
